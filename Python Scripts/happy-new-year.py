import turtle
t = turtle.Pen()
t.shape('turtle')
t.width(20)
t.speed(8)
t.up()
t.goto(-190,170)
t.down()

def h():
	t.color('blue')
	t.lt(90)
	t.fd(100)
	t.back(50)
	t.rt(90)
	t.fd(50)
	t.lt(90)
	t.fd(50)
	t.back(100)

